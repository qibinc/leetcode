MOD = 10 ** 9 + 7


class Solution:
    def countPaths(self, n, road):
        f = [[0] * n for _ in range(n)]
        dist = [[10 ** 20] * n for _ in range(n)]
        for i in range(n):
            f[i][i] = 1
            dist[i][i] = 0
        for i, j, d in road:
            dist[i][j] = d
            dist[j][i] = d
            f[i][j] = 1
            f[j][i] = 1
        for t in range(n):
            for i in range(n):
                for j in range(n):
                    if i == j or t == i or t == j:
                        continue
                    if dist[i][t] + dist[t][j] < dist[i][j]:
                        dist[i][j] = dist[i][t] + dist[t][j]
                        f[i][j] = f[i][t] * f[t][j]
                    elif dist[i][t] + dist[t][j] == dist[i][j]:
                        f[i][j] += f[i][t] * f[t][j]
        return f[0][n - 1] % MOD


